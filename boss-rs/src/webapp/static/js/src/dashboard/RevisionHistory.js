function RevisionHistory(data){
	this.push();
	this.fromData(data);

	return this;
}

RevisionHistory.prototype = [];

RevisionHistory.prototype.fromData = function(data){
	var that = this;
	data.forEach(function(rev){
		that[rev.newId.name] = rev;
		that.push(rev.newId.name);
	});

	this.forEach(function(name){
		var rev = that[name],
		parent;
		rev.parent = parent = that[rev.oldId.name];

		if(parent){
			parent.children = parent.children || [];
			if(!~parent.children.indexOf(rev)){
				parent.children.push(rev);
			}
		}
	});
};

